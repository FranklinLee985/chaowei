public class Trophy {

    String companyid;
    String trophyname;
    String trophyimg;
    String trophyvoice;
    String trophyIcon;
    String trophyeffect;

    public Trophy() {
    }

    public String getCompanyid() {
        return this.companyid;
    }

    public void setCompanyid(String companyid) {
        this.companyid = companyid;
    }

    public String getTrophyname() {
        return this.trophyname;
    }

    public void setTrophyname(String trophyname) {
        this.trophyname = trophyname;
    }

    public String getTrophyimg() {
        return this.trophyimg;
    }

    public void setTrophyimg(String trophyimg) {
        this.trophyimg = trophyimg;
    }

    public String getTrophyvoice() {
        return this.trophyvoice;
    }

    public void setTrophyvoice(String trophyvoice) {
        this.trophyvoice = trophyvoice;
    }

    public String getTrophyIcon() {
        return this.trophyIcon;
    }

    public void setTrophyIcon(String trophyIcon) {
        this.trophyIcon = trophyIcon;
    }

    public String getTrophyeffect() {
        return this.trophyeffect;
    }

    public void setTrophyeffect(String trophyeffect) {
        this.trophyeffect = trophyeffect;
    }

}
